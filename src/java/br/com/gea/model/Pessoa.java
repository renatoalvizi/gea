package br.com.gea.model;

/**
 *
 * @author renat
 */
public class Pessoa {
    private int idPessoa;
    private String nomeRazaoPessoa;
    private String rgIePessoa;
    private String cpfCnpjPessoa;
    private String dataNascFundPessoa;
    private String telefonePessoa;
    private String logradouroPessoa;
    private String numLogradouroPessoa;
    private String bairroPessoa;
    private String cepPessoa;
    private String emailPessoa;
    private String senhaPessoa;    

    public Pessoa() {
    }

    public Pessoa(int idPessoa, String nomeRazaoPessoa, String rgIePessoa, String cpfCnpjPessoa, String dataNascFundPessoa, String telefonePessoa, String logradouroPessoa, String numLogradouroPessoa, String bairroPessoa, String cepPessoa, String emailPessoa, String senhaPessoa) {
        this.idPessoa = idPessoa;
        this.nomeRazaoPessoa = nomeRazaoPessoa;
        this.rgIePessoa = rgIePessoa;
        this.cpfCnpjPessoa = cpfCnpjPessoa;
        this.dataNascFundPessoa = dataNascFundPessoa;
        this.telefonePessoa = telefonePessoa;
        this.logradouroPessoa = logradouroPessoa;
        this.numLogradouroPessoa = numLogradouroPessoa;
        this.bairroPessoa = bairroPessoa;
        this.cepPessoa = cepPessoa;
        this.emailPessoa = emailPessoa;
        this.senhaPessoa = senhaPessoa;
    }

    
    
    /**
     * @return the idPessoa
     */
    public int getIdPessoa() {
        return idPessoa;
    }

    /**
     * @param idPessoa the idPessoa to set
     */
    public void setIdPessoa(int idPessoa) {
        this.idPessoa = idPessoa;
    }

    /**
     * @return the nomeRazaoPessoa
     */
    public String getNomeRazaoPessoa() {
        return nomeRazaoPessoa;
    }

    /**
     * @param nomeRazaoPessoa the nomeRazaoPessoa to set
     */
    public void setNomeRazaoPessoa(String nomeRazaoPessoa) {
        this.nomeRazaoPessoa = nomeRazaoPessoa;
    }

    /**
     * @return the rgIePessoa
     */
    public String getRgIePessoa() {
        return rgIePessoa;
    }

    /**
     * @param rgIePessoa the rgIePessoa to set
     */
    public void setRgIePessoa(String rgIePessoa) {
        this.rgIePessoa = rgIePessoa;
    }

    /**
     * @return the cpfCnpjPessoa
     */
    public String getCpfCnpjPessoa() {
        return cpfCnpjPessoa;
    }

    /**
     * @param cpfCnpjPessoa the cpfCnpjPessoa to set
     */
    public void setCpfCnpjPessoa(String cpfCnpjPessoa) {
        this.cpfCnpjPessoa = cpfCnpjPessoa;
    }

    /**
     * @return the dataNascFundPessoa
     */
    public String getDataNascFundPessoa() {
        return dataNascFundPessoa;
    }

    /**
     * @param dataNascFundPessoa the dataNascFundPessoa to set
     */
    public void setDataNascFundPessoa(String dataNascFundPessoa) {
        this.dataNascFundPessoa = dataNascFundPessoa;
    }

    /**
     * @return the telefonePessoa
     */
    public String getTelefonePessoa() {
        return telefonePessoa;
    }

    /**
     * @param telefonePessoa the telefonePessoa to set
     */
    public void setTelefonePessoa(String telefonePessoa) {
        this.telefonePessoa = telefonePessoa;
    }

    /**
     * @return the logradouroPessoa
     */
    public String getLogradouroPessoa() {
        return logradouroPessoa;
    }

    /**
     * @param logradouroPessoa the logradouroPessoa to set
     */
    public void setLogradouroPessoa(String logradouroPessoa) {
        this.logradouroPessoa = logradouroPessoa;
    }

    /**
     * @return the numLogradouroPessoa
     */
    public String getNumLogradouroPessoa() {
        return numLogradouroPessoa;
    }

    /**
     * @param numLogradouroPessoa the numLogradouroPessoa to set
     */
    public void setNumLogradouroPessoa(String numLogradouroPessoa) {
        this.numLogradouroPessoa = numLogradouroPessoa;
    }

    /**
     * @return the bairroPessoa
     */
    public String getBairroPessoa() {
        return bairroPessoa;
    }

    /**
     * @param bairroPessoa the bairroPessoa to set
     */
    public void setBairroPessoa(String bairroPessoa) {
        this.bairroPessoa = bairroPessoa;
    }

    /**
     * @return the cepPessoa
     */
    public String getCepPessoa() {
        return cepPessoa;
    }

    /**
     * @param cepPessoa the cepPessoa to set
     */
    public void setCepPessoa(String cepPessoa) {
        this.cepPessoa = cepPessoa;
    }

    /**
     * @return the emailPessoa
     */
    public String getEmailPessoa() {
        return emailPessoa;
    }

    /**
     * @param emailPessoa the emailPessoa to set
     */
    public void setEmailPessoa(String emailPessoa) {
        this.emailPessoa = emailPessoa;
    }

    /**
     * @return the senhaPessoa
     */
    public String getSenhaPessoa() {
        return senhaPessoa;
    }

    /**
     * @param senhaPessoa the senhaPessoa to set
     */
    public void setSenhaPessoa(String senhaPessoa) {
        this.senhaPessoa = senhaPessoa;
    }
  
}
